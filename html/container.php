	<!--Liste des ingrédients-->
		<div class="blocheader2">
			<h3 id="title">Les ingrédients:</h3>
			<ul>
				<li>600gr de graine de couscous moyen</li>
				<li>1kg de viande d'agneau</li>
				<li>6 cuisses de poulet</li>
				<li>12 merguez</li>
				<li>2 oignons</li>
				<li>3 courgettes</li>
				<li>3 navets</li>
				<li>4 carottes</li>
				<li>2 branches de céléri</li>
				<li>1 grande boîte de chair de tomate</li>
			</ul>
			<ul>
				<li>1 petite boîte de concentré de tomate</li>
				<li>1 grande boîte de pois chiche</li>
				<li>3 poignées de raisins sultanine</li>
				<li> 1 càc de gingembre</li>
				<li>1 càc de curcuma</li>
				<li>1 càc de piment fort</li>
				<li>1 càc de raz el hanout</li>
				<li>1 càc de safran</li>
				<li>1 botte de coriandre</li>
				<li>harissa</li>
			</ul>
		</div>

		<!--La préparation numérotée par étape-->
		<div class="blocheader3">
			<h3>La préparation:</h3>
			<ol>
				<li>Faire tremper les raisins dans un bol d'eau</li>
				<li>Faire revenir les oignons avec le poulet, l'agneau et les
				épices au fond de la marmitte. <br /> Saler et poivrer. Laisser
				revenir jusqu'à coloration de la viande.</li>
				<li>Ajouter l'équivalent de 2 litres d'eau, la chair et le coulis
				de tomate</li>
				<li>Eplucher et couper les légumes en cubes. Ajouter les à la
				préparation en réservant les courgettes.</li>
				<li>Laisser mijoter 20 minutes</li>
				<li>Ajouter la coriandre fraîche, les pois chiche,les raisins et
				les courgettes</li>
				<li>Cuire les merguez au four ou à la poêle</li>
				<li>Vous pouvez faire une sauce dans un bol avec le jus du couscous
				et de l'harissa</li>
			</ol>
		</div>
